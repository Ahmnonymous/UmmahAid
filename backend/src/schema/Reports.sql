Total Financial Assistance

SELECT 
    WA.NAME,
    WA.SURNAME,
    WA.FILE_NUMBER,
    WA.FILE_CONDITION_ID,
    WA.FILE_STATUS_ID,
    WA.EMPLOYMENT_STATUS_ID,
    WA.HEALTH_ID,
    WA.CELL_NUMBER,
    COALESCE(SUM(FA.FINANCIAL_COST), 0) AS FINANCIAL_FOOD_ASSISTANCE,
    COALESCE(SUM(AT.FINANCIAL_AMOUNT), 0) AS FINANCIAL_TRANSACTIONS,
    COALESCE(SUM(FA.FINANCIAL_COST), 0) + COALESCE(SUM(AT.FINANCIAL_AMOUNT), 0) AS TOTAL_FINANCIAL
FROM 
    WELFARE_APPLICANT_DETAILS WA
LEFT JOIN 
    APPLICANT_FOOD_ASSISTANCE FA ON FA.WELFARE_APPLICANT_DETAIL_ID = WA.ID
LEFT JOIN 
    APPLICANT_TRANSACTIONS AT ON AT.WELFARE_APPLICANT_DETAIL_ID = WA.ID
GROUP BY
    WA.NAME,
    WA.SURNAME,
    WA.FILE_NUMBER,
    WA.CELL_NUMBER,
    WA.FILE_CONDITION_ID,
    WA.FILE_STATUS_ID,
    WA.EMPLOYMENT_STATUS_ID,
    WA.HEALTH_ID;

Financial Assistance
SELECT 
    WA.NAME,
    WA.SURNAME,
    WA.FILE_NUMBER,
    WA.CELL_NUMBER,

    AT.ASSISTED_BY,
    AT.FINANCIAL_AMOUNT,
    AT.ASSISTANCE_TYPE_ID,
    AT.DATE_OF_ASSITANCE,
    AT.CREATED_BY,
    AT.DATESTAMP
FROM 
            APPLICANT_TRANSACTIONS AT
INNER JOIN 
    WELFARE_APPLICANT_DETAILS WA ON AT.WELFARE_APPLICANT_DETAIL_ID = WA.ID;

