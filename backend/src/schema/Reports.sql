Relationship Report
SELECT 
    WA.NAME,
    WA.SURNAME,
    WA.FILE_NUMBER,
    WA.CELL_NUMBER,

    WR.RELATIONSHIP_TYPE_ID,
    WR.NAME as Relatives_Name,
    WR.SURNAME as Relatives_Surname ,
    WR.EMPLOYMENT_STATUS,
    WR.GENDER,
    TRUNC(MONTHS_BETWEEN(SYSDATE, WR.DOB)/12) AS AGE,
    WR.HIGHEST_EDUCATION,
    WR.Marital_STATUS,
    WR.HEALTH_CONDITION
FROM 
            APPLICANT_RELATIONSHIP WR
INNER JOIN 
    WELFARE_APPLICANT_DETAILS WA ON WR.WELFARE_APPLICANT_DETAIL_ID = WA.ID;


